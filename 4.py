def times(x):
    x = str(x) + "*"
    return x
def minus(x):
    x = str(x) + "-"
    return x
def plus(x):
    x = str(x) + "+"
    return x
def operation(y, clct, ar):
    if clct == "*":
        return ar * y
    elif clct == "-":
        return y - ar
    elif clct == "+":
        return ar + y
def zero(x=10):
    if x == 10:
        return 0
    else:
        y = 0
        ar = int(x[0])
        clct = x[1]
        print(operation(y, clct, ar))
